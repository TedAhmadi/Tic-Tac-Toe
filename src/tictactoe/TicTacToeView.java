    private void chatConnection(ServerClient sORc){
        
        if(sORc == ServerClient.server){
            try{
                sSocket_chat = new ServerSocket(chatPort);
            }catch(IOException e){
                System.out.println("Chat Error:Could not listen on port: "+ chatPort);
                JOptionPane.showMessageDialog(null, "Chat Error:Could not listen on port: "+ chatPort, null, 0);
                System.exit(1);
            }
            try {
                socket_chat = sSocket_chat.accept();
            } catch (IOException e) {
                System.out.println("Chat Error:Accept failed on port: "+ chatPort);
                JOptionPane.showMessageDialog(null, "Chat Error:Accept failed on port: "+ chatPort, null, 0);
                System.exit(1);
            }
        }
        else if(sORc == ServerClient.client){
            try {
                socket_chat = new Socket(player1IpAddress, chatPort);
            } catch (UnknownHostException e) {
                System.err.println("Chat Error:Unknown host: "+player1IpAddress);
                JOptionPane.showMessageDialog(null, "Chat Error:Unknown host: "+player1IpAddress, null, 0);
                System.exit(1);
            } catch (IOException e) {
                System.err.println("Chat Error:Could not get I/O from server: "+player1IpAddress);
                JOptionPane.showMessageDialog(null, "Chat Error:Could not get I/O from server: "+player1IpAddress, null, 0);
                System.exit(1);
            }
        }
        
        chatCommunication = new ChatCommunication(socket_chat, jTextArea1);
        Thread thRead = new Thread(chatCommunication);
        thRead.start();
        
    }
    
    protected void finalize(){
        try{
            socket_chat.close();
            sSocket_chat.close();
        } catch (IOException e) {
            System.out.println("Chat Error:Could not close socket");
            JOptionPane.showMessageDialog(null, "Chat Error:Could not close socket", null, 0);
            System.exit(1);
        }
    }
      
