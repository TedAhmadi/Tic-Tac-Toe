    public void nextMove(Byte tile){
        
        JToggleButton tileButton = null;
        tileButton = tileArray[tile-1];
        
        tttPlayer activePlayer = game.getActivePlayer();
        String piece = null;
        if(activePlayer.getPiece()== Pieces.x)
            piece = "X";
        else if(activePlayer.getPiece()== Pieces.o)
            piece = "O";
        
        tileButton.setSelected(true);
        tileButton.setText(piece);
        tileButton.setEnabled(false);  
       
        if(activePlayer == game.getThisPlayer())
            setTilesEnabled(false);
        else
            setTilesEnabled(true);
        
        tttPlayer winner = game.assignTile(tile);
        
        if(winner!=null)
        {
            jLabel6.setText(Integer.toString(game.getPlayer1().getWins()) );
            jLabel7.setText(Integer.toString(game.getPlayer2().getWins()) );
            
            resetGame();
            
            JOptionPane.showMessageDialog(mainPanel, winner.getName()+" wins");
        }
        else if(game.getPlayer1().getTile().size()+game.getPlayer2().getTile().size()==9)
        {
            game.reset();
            resetGame();
        }
    }
    
    private void resetGame() {
        for(int i=0; i<9; i++){
            tileArray[i].setText(null);
            tileArray[i].setSelected(false);
            tileArray[i].setEnabled(true);
        }
        
        if(game.getActivePlayer() == game.getThisPlayer())
            setTilesEnabled(true);
        else
            setTilesEnabled(false);
    }
    
    private void setTilesEnabled(boolean enabled){
        for(Byte i=1; i<=9; i++){
            if(!tileArray[i-1].isSelected())
                tileArray[i-1].setEnabled(enabled);
        }
    }
    
